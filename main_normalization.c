void normalize_sensor_values(int adc_buf[], int sensors_min[], int sensors_max[], float normalized_values[])
{
	for (int i = 0; i < NUM_SENSORS; i++)
	{
		normalized_values[i] = (float)(adc_buf[i] - sensors_min[i]) / (sensors_max[i] - sensors_min[i]);
	}
}
void record_sensor_values(int adc_buf[], int sensors_min_black[], int sensors_max_white[]) {
// Initialize sensors_min_black and sensors_max_white
	for (int i = 0; i < NUM_SENSORS; i++) {
		sensors_min_black[i] = MAX_SENSOR_VALUE;
		sensors_max_white[i] = MIN_SENSOR_VALUE;
	}
	while (HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_13) == GPIO_PIN_RESET) {
		for (int i = 0; i < NUM_SENSORS; i++) {
				if (adc_buf[i] < sensors_min_black[i]) {
				sensors_min_black[i] = adc_buf[i];
			}
				if (adc_buf[i] > sensors_max_white[i]) {
				sensors_max_white[i] = adc_buf[i];
			}
		}
	}
}

